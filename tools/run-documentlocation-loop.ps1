        & powershell.exe -NoProfile -ExecutionPolicy Bypass -File $WatchdogPath -DataverseUrl $DataverseUrl -UseAzureCliToken | Out-Null
        $exitCode = $LASTEXITCODE
        $healthPath = Join-Path (Split-Path -Parent $WatchdogPath) "logs\sharepoint_link_health.json"
        $health = if (Test-Path -LiteralPath $healthPath) { Get-Content -LiteralPath $healthPath -Raw | ConvertFrom-Json } else { $null }
        $latestEvidence = Get-ChildItem -LiteralPath $EvidenceDir -Filter "sharepoint_link_watchdog_*.json" -ErrorAction SilentlyContinue |
            Sort-Object LastWriteTime -Descending |
            Select-Object -First 1

        $Result.watchdog = [ordered]@{
            exitCode = $exitCode
            health = $health.health
            locationsReviewed = $health.locationsReviewed
            evidence = $latestEvidence.FullName
        }
    }

    $Result.validation = [ordered]@{
        anchorLogicalName = $anchorLogicalName
        anchorId = $anchorId
        duplicateDocumentLocationsForRegarding = ($postLocations.Count -gt 1)
        documentLocationCountForRegarding = if ($lookupDelayed) { 1 } else { $postLocations.Count }
        lookupDelayedButDirectReadbackOk = $lookupDelayed
        graphOk = [bool]$Result.graph.ok
        folderCreated = $false
        documentLocationCreated = [bool]$Result.documentlocation.created
        documentLocationReused = [bool]$Result.documentlocation.reused
    }

    if ($Result.validation.duplicateDocumentLocationsForRegarding) {
        $Result.outcome = "FAIL_DUPLICATE"
    }
    elseif (-not $Result.graph.ok) {
        $Result.outcome = "FAIL_GRAPH"
    }
    else {
        $Result.outcome = "PASS"
    }

    Append-Telemetry
    Save-LastState
    Save-Evidence
    Write-ExecutiveOutput
    [pscustomobject]@{
        runId = $RunId
        outcome = $Result.outcome
        eventKey = $EventKey
        anchorLogicalName = $anchorLogicalName
        anchorId = $anchorId
        evidence = $EvidencePath
        activityid = $activityId
        expedienteid = $Result.expediente.expedienteid
        sharepointdocumentlocationid = $docLocationId
        anchorCreated = $Result.anchor.created
        anchorReused = $Result.anchor.reused
        pocCreated = $Result.poc.created
        pocReused = $Result.poc.reused
        expedienteCreated = $Result.expediente.created
        expedienteReused = $Result.expediente.reused
        documentLocationCreated = $Result.documentlocation.created
        documentLocationReused = $Result.documentlocation.reused
        duplicateDocumentLocationsForRegarding = $Result.validation.duplicateDocumentLocationsForRegarding
        health = $Result.watchdog.health
        locationsReviewed = $Result.watchdog.locationsReviewed
        watchdogEvidence = $Result.watchdog.evidence
        telemetryPath = $TelemetryPath
        lastStatePath = $LastStatePath
    } | ConvertTo-Json -Depth 6
}
catch {
    $Result.outcome = "FAIL"
    $Result.errors += [ordered]@{
        message = $_.Exception.Message
        at = (Get-Date).ToString("o")
        }
    Save-Evidence
    try {
        Append-Telemetry
        Save-LastState
    }
    catch {
        # Do not hide the original failure if secondary logging fails.
    }
    Write-ExecutiveOutput
    [pscustomobject]@{
        runId = $RunId
        outcome = "FAIL"
        eventKey = $EventKey
        anchorLogicalName = $Result.anchor.logicalName
        anchorId = $Result.anchor.id
        evidence = $EvidencePath
        error = $_.Exception.Message
        activityid = $Result.poc.activityid
        expedienteid = $Result.expediente.expedienteid
        sharepointdocumentlocationid = $Result.documentlocation.sharepointdocumentlocationid
    } | ConvertTo-Json -Depth 6
    exit 1
}
